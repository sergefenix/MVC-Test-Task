
-- Таблица товаров --
create table `tasks` (
    `id` int(10) unsigned not null auto_increment,
    `email` varchar(255) not null,
    `name`  varchar(255) not null,
    `text`  varchar(255) not null,
    `img`   varchar(255) not null,,
    primary key (id)
)

auto_increment = 1
character set utf8
collate utf8_general_ci;


